fn main() {
    let numero: i32 = 6;

    if numero == 1 {
        println!("Lunes");
    } else if numero == 2 {
        println!("Martes");
    } else if numero == 3 {
        println!("Miércoles");
    } else if numero == 4 {
        println!("Jueves");
    } else if numero == 5 {
        println!("Viernes");
    } else if numero == 6 {
        println!("Sábado");
    } else if numero == 7 {
        println!("Domingo");
    } else {
        println!("Número no válido.");
    }
}
